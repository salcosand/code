package com.drawing.datastructure;

/**
 * Enumeration of possible gesture types that can be recognized by the $1 - approach
 * @author Christian Brändel TU Dresden / SAP Research Dresden
 *
 */
public enum GestureTypes {

	/**
	 * No Gesture could be recognized
	 */
	NOGESTURE,
	/**
	 * Circle Gesture
	 */
	TOPIC,
	/**
	 * Rectangle Gesture
	 */
	COMPANY,
	/**
	 * Person Gesture
	 */
	PERSON,
	/**
	 * Triangle Gesture
	 */
	EVENT,
	/**
	 * Check Gesture
	 */
	CHECK,
	
	NEWTYPE,
	
	ORGANIZATIONTYPE,
	
	TOPICTYPE, 
	
	MATCHCONTACT
	
	
}
